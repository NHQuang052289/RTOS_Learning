void TimerCallback(xTimerHandle xTimer)
{
	if(xTimer == PTHandler)	//if callback of Periodic timer
	{
		printf("Send from callback of PT\n\n");
	}
	else if(xTimer == OTHandler)	//if callback of Once timer
	{
		HAL_GPIO_WritePin(LED_GPIO_Port, LED_Pin, GPIO_PIN_RESET);	//turn off led
		printf("Off led by callback of OT\n\n");
	}
}
