/* init_task_02 function */
void init_task_02(void const * argument)
{
  /* USER CODE BEGIN init_task_02 */
	uint8_t index2 = 0;
  /* Infinite loop */
  for(;;)
  {
		printf("task 2 = %d\n", index2++);
		if(index2 == 3)
		{
			printf("task 1 suppend\n");
			osThreadSuspend(myTask01Handle);	
		}
		if(index2 == 6)
		{
			printf("task2 delay until\n");
			uint32_t PreviousWakeTime = osKernelSysTick();
		  osDelayUntil(&PreviousWakeTime, 4000);
		}
		if(index2 == 8)
		{
			printf("task2 take binSema\n");
			osSemaphoreWait(binSemaHandle, osWaitForever);
		}
		if(index2 == 16)
		{
			printf("task 2 give binSema\n");
			osSemaphoreRelease(binSemaHandle);
		}
		osDelay(1000);
  }
  /* USER CODE END init_task_02 */
}

